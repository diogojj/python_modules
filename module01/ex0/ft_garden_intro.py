#!/usr/bin/env python3


def main() -> None:
    name: str = "zucchini"
    plant_type: str = "vegetable"
    height: int = 10
    age: int = 22

    print("=== Welcome to the Garden ===")
    print(f"Plant: {name}")
    print(f"Type: {plant_type}")
    print(f"Height: {height} cm")
    print(f"Age: {age} days")
    print("=== End of Program ===")


if __name__ == "__main__":
    main()
